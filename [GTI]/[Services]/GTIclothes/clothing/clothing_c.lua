								guiGridListSetItemText(cartGrid, cartRow, 4, dff, false, false)
								guiGridListSetItemData(cartGrid, cartRow, 4, dff)
								guiGridListSetItemData(cartGrid, cartRow, 3, txd)
								guiGridListSetItemData(cartGrid, cartRow, 1, i)
							end
						end
					end
				end
			end
		end
	end
